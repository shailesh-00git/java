
class IfElse {

    public static void main(String[] args) {
        int a = 2, b = 3;
        if (a < b) {
            System.out.println("a isless than b");
        }
        a = a + 1;
        if (a == b) {
            System.out.println("a is equals to b");
        }
        a = a * 2;
        if (a > b) {
            System.out.println("a is greater than b");
        }

    }
}
